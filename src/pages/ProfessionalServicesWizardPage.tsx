/**
 * Professional Services Wizard Page
 * Entry point for the Service Preferences Wizard (5-step multi-select flow)
 */

import { ServicePreferencesWizard } from '@/components/professional/ServicePreferencesWizard';
import { useAuth } from '@/hooks/useAuth';
import { Navigate } from 'react-router-dom';
import { Loader2 } from 'lucide-react';

const ProfessionalServicesWizardPage = () => {
  const { user, loading } = useAuth();

  if (loading) {
    return (
      <div className="min-h-screen flex items-center justify-center">
        <Loader2 className="h-8 w-8 animate-spin text-muted-foreground" />
      </div>
    );
  }

  if (!user) {
    return <Navigate to="/auth" replace />;
  }

  return <ServicePreferencesWizard />;
};

export default ProfessionalServicesWizardPage;
